package org.javautil.document.style;

/**
 * Possible values for an underline style; not all values are necessarily
 * possible by the underlying document style.
 * 
 * @author bcm
 */
public enum FontUnderlineStyle {

	/**
	 * No underline
	 */
	NONE,

	/**
	 * A single solid underline
	 */
	SINGLE,

	/**
	 * A double solid underline
	 */
	DOUBLE

}
